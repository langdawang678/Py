'''
列表操作
'''

# 遍历列表
magicians=['aa','bb','cc','dd']
for magician in magicians:
    print(magician)
print('over')


# 数值列表，含头不含尾
for value in range(1,5):
    print(value)

# 使用range() 创建数字列表
numbers=list(range(1,6))
print(numbers)

# 指定步长
numbers2=list(range(1,10,2))
print(numbers2)

